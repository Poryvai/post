package com.poryvai.post.model;

/**
 * Enumeration representing the possible positions/roles for an employee.
 */
public enum EmployeePosition {

    /**
     * Represents a General Clerk or Operator.
     * This role typically involves handling routine office tasks, sorting mail,
     * assisting customers at the counter, and basic data entry.
     */
    CLERK,
    /**
     * Represents a Manager.
     * This role is responsible for overseeing daily operations, managing staff,
     * ensuring service quality, and handling administrative duties within a post office.
     */
    MANAGER,
    /**
     * Represents a Driver.
     * This role involves the transportation of parcels and mail between post offices
     * or directly to customer locations, requiring driving and logistical skills.
     */
    DRIVER,
    /**
     * Represents an Accountant.
     * This role is responsible for managing financial records, processing payments,
     * preparing financial reports, and ensuring compliance with accounting standards.
     */
    ACCOUNTANT,
    /**
     * Represents an Administrator.
     * This role typically handles system administration, office support,
     * record keeping, and coordination tasks.
     */
    ADMINISTRATOR
}
